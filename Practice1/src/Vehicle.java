
public class Vehicle {
	
	String make;
	String model;
	String color;
	int year;
	double price;
	
	public Vehicle(String make, String model, String color, int year, double price) {
		super();
		this.make = make;
		this.model = model;
		this.color = color;
		this.year = year;
		this.price = price;
	}

}
